namespace Topten.RichText
{
    /// <summary>
    /// Alignment of text within a text block
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Use base direction of the text block
        /// </summary>
        Auto,

        /// <summary>
        /// Left-aligned
        /// </summary>
        Left,

        /// <summary>
        /// Centered
        /// </summary>
        Center,

        /// <summary>
        /// Right aligneed
        /// </summary>
        Right,
    }
}
